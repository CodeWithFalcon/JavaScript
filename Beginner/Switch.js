/* Made Switch for Days Statements */
let age = prompt("What is your age?");
switch (age) {
  case "12":
    console.log("Your Age is 12.");
    break;
  case "13":
    console.log("Your Age is 13.");
    break;
  case "14":
    console.log("Your Age is 14.");
    break;
  case "15":
    console.log("Your Age is 15.");
    break;
  default:
    console.log("Your Age is Special!");
    break;
}
